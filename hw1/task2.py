print(5 & 6) # 0000 0101 & 0000 0110 = 0000 0100 -> 4
print(5 | 6) # 0000 0101 | 0000 0110 = 0000 0111 -> 7
print(5 ^ 6) # 0000 0101 ^ 0000 0110 = 0000 0011 -> 3
print(~5) # прямой код: 0101, обратный код: 1010 , доп.код: 1011 (11 в десятичной системе) => 5 - 11 = -6
print(5 << 2) # 5 * 2**2 = 20
print(5 >> 2) # 5 // 2**2 = 1

